int _replaceChar(int charCode) {
  if (charCode < _diacriticsCharcodeStart ||
      charCode > _diacriticsCharcodeEnd) {
    return charCode;
  }
  return _charcodeReplaceMapping[charCode - _diacriticsCharcodeStart] ??
      charCode;
}

/// Replaces diacritical marks in [str] with their ASCII equivalents.
///
/// Characters outside the Latin Extended range (U+00C0 to U+017F) pass
/// through unchanged.
String replaceDiacritics(String str) {
  final charCodes = <int>[];
  for (var i = 0; i < str.length; i++) {
    charCodes.add(_replaceChar(str.codeUnitAt(i)));
  }
  return String.fromCharCodes(charCodes);
}
